/**
 * L3 · Knowledge Attention Controller
 * 职责：规定 agent 在回答时的信息来源优先级，防止用通用知识压过项目上下文
 * 输入：ctx（无强依赖）
 * 输出：prompt 文本块
 * 关系：与 L2/kb-sync.js 协同——kb-sync 决定什么时候读 KB，此模块决定读到后怎么优先使用
 * Fallback：注入最小优先级声明
 */
(function () {
  'use strict';

  var PROMPT = (
    '【知识注意力控制器】\n' +

    '── 信息来源优先级（从高到低）──\n' +
    '1. 用户当前消息中的明确指令与约束（最高，无条件覆盖下层）\n' +
    '2. 当前 session 的任务上下文（正在做什么、已确认的决策）\n' +
    '3. 系统注入的项目数据（PRIMARY_REF：任务列表、每日任务、枝条、积分）\n' +
    '4. 系统注入的知识库内容（KB 摘要与节选）\n' +
    '5. 长期记忆（LTM 中的历史事实）\n' +
    '6. 通用背景知识（最低，仅在上层无相关信息时补充）\n\n' +

    '── 项目关键词触发规则 ──\n' +
    '当用户提到以下关键词时，优先检索项目上下文而非给出通用解释：\n' +
    '「任务」「每日任务」「枝条」「积分」「知识库」「AETHER」「AMADEUS」「进展」「branch」「credits」\n' +
    '例：用户说"任务"时，先看 PRIMARY_REF 里有没有对应任务，而不是给出"任务的一般定义"。\n\n' +

    '── 用户行为词汇识别 ──\n' +
    '随着对话积累，识别用户频繁使用的术语、文件名、函数名、项目专有名词，\n' +
    '在后续回复中优先以用户自己的词汇体系作答，不要用不同名字指代同一概念。\n\n' +

    '── 防误联想规则 ──\n' +
    '不要把普通技术术语（如"hook"、"store"、"action"）自动联想到与本项目无关的框架或库。\n' +
    '先判断用户说的词在本项目中有没有特定含义，再决定是否套用通用解释。'
  );

  var FALLBACK = '【注意力】项目数据优先于通用知识，用户指令优先于一切，项目关键词触发项目上下文优先检索。';

  window.AMADEUS_L3_Attention = {
    name: 'L3_Attention',
    layer: 'L3',
    build: function () { return PROMPT; },
    fallback: FALLBACK,
  };
})();
