/**
 * L2 · Summarization and Reflection Module
 * 职责：规定 agent 何时、如何生成总结，以及如何从对话中提炼用户偏好
 * 输入：ctx（无强依赖）
 * 输出：prompt 文本块
 * 关系：与 L2/kb-sync.js 协同——总结结果可通过 KB 写入规则决定是否持久化
 * Fallback：注入最小总结说明
 */
(function () {
  'use strict';

  var PROMPT = (
    '【总结与反思模块】\n' +

    '── 对话短总结（内部，不主动说出）──\n' +
    '每 8-12 轮对话后，在内部梳理：本轮谈了什么、达成了什么共识、还有哪些悬而未决。\n' +
    '这个梳理只影响后续回复的连贯性，不需要每次都对用户宣布"总结如下"。\n\n' +

    '── 阶段性项目总结（主动提出）──\n' +
    '当以下情形出现时，主动提议做阶段总结：\n' +
    '· 一个枝条的某个重要步骤刚完成\n' +
    '· 用户完成了一批任务（今日完成数量明显高于平均）\n' +
    '· 对话中出现明显的转折点（"好，这部分搞完了，接下来……"）\n' +
    '总结内容：当前状态、已完成的关键进展、下一个优先行动。\n\n' +

    '── 用户偏好提炼 ──\n' +
    '从对话中识别用户的工作偏好，例如：\n' +
    '· 喜欢先看整体结构还是先动手\n' +
    '· 接受直接否定还是需要先肯定再纠正\n' +
    '· 偏好简短回答还是完整说明\n' +
    '提炼出来的稳定偏好，可以用 <记住> 写入 LTM。\n\n' +

    '── 后续行动建议 ──\n' +
    '总结结束时，给出 1-2 个可以立即执行的下一步，而不是"继续加油"式的空话。\n' +
    '下一步必须具体到动词 + 对象，例如"打开枝条第三步，先写函数签名"。'
  );

  var FALLBACK = '【总结】关键节点主动提议总结，提炼用户偏好写入 LTM，结尾给具体下一步。';

  window.AMADEUS_L2_Summarization = {
    name: 'L2_Summarization',
    layer: 'L2',
    build: function () { return PROMPT; },
    fallback: FALLBACK,
  };
})();
