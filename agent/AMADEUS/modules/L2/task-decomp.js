/**
 * L2 · Task Decomposition Engine
 * 职责：规定 agent 如何识别用户真实意图并将目标拆解为可执行子任务
 * 输入：ctx（无强依赖）
 * 输出：prompt 文本块
 * 关系：为 L2/ltask-manager.js 提供基础分解逻辑；拆出的任务通过 actions.js 落库
 * Fallback：注入最小拆解说明
 * 使用场景：用户新建任务时 AI 拆分 / chatbox 中让 AI 创建并拆分任务
 */
(function () {
  'use strict';

  var PROMPT = (
    '【任务拆解引擎】\n' +

    '── 意图识别 ──\n' +
    '用户说"我要做 X"时，先判断 X 是目标还是任务：\n' +
    '· 目标（Goal）：模糊的、长期的，例如"我想提高英语"——需要拆解\n' +
    '· 任务（Task）：具体的、可执行的，例如"背 30 个单词"——可直接创建\n' +
    '当用户表达含糊时，先问清楚"你说的是这周内要完成，还是长期进行？"再拆。\n\n' +

    '── 三类任务识别 ──\n' +
    '主任务（Main Task）：有明确截止时间或一次性完成的工作\n' +
    '副任务（Subtask）：主任务的组成步骤，完成后自动归入父任务\n' +
    '隐含约束（Implicit Constraint）：用户没说但必须满足的条件，例如"不能花太多时间""不影响睡眠"\n' +
    '拆解时必须把隐含约束标出来，作为执行计划的边界条件。\n\n' +

    '── 执行计划生成 ──\n' +
    '拆解后提供：可执行的第一步（动词开头）、预计时间、依赖关系（如果有）。\n' +
    '计划里的每一步必须是可以立即动手的，不能是"想清楚方向"这种无法执行的步骤。\n\n' +

    '── 不确定信息处理 ──\n' +
    '当信息不足时，标出哪些变量还没确定，先用占位符（如"时间待定""资源待确认"）。\n' +
    '不允许在信息不足的情况下直接给出貌似完整的执行计划——要么问清楚，要么标出哪些是假设。\n\n' +

    '── 创建任务触发条件 ──\n' +
    '用户明确同意创建时，使用 <aether_action> 落库，格式见 PRIMARY_REF 中的写权限说明。\n' +
    '不要在用户没有明确确认的情况下自动创建任务，先口头说明拆解方案，等用户确认。'
  );

  var FALLBACK = '【任务拆解】识别真实意图，区分主任务/子任务/隐含约束，信息不足时标出而非编造，用户确认后再落库。';

  window.AMADEUS_L2_TaskDecomp = {
    name: 'L2_TaskDecomp',
    layer: 'L2',
    build: function () { return PROMPT; },
    fallback: FALLBACK,
  };
})();
