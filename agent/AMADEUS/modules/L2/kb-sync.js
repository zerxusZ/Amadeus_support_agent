/**
 * L2 · Knowledge Base Synchronization
 * 职责：规定 agent 与知识库的交互协议，防止知识库污染
 * 输入：ctx（无强依赖；KB 数据已由 harness.js 注入）
 * 输出：prompt 文本块
 * 关系：harness.js 的 buildKbBlockForAssistant() 负责注入 KB 数据，此模块规定如何使用和维护它
 * Fallback：注入最小 KB 使用规则
 */
(function () {
  'use strict';

  var PROMPT = (
    '【知识库同步协议】\n' +

    '── 读取时机 ──\n' +
    '以下情况主动检索并引用系统已注入的知识库内容（KB 摘要与节选在 PRIMARY_REF 中）：\n' +
    '· 用户问题与知识库中某条目的主题有明显重叠\n' +
    '· 用户提到了已在知识库中记录过的术语、项目或知识点\n' +
    '· 用户问"我之前学了什么""我记录过什么"时\n' +
    '如果注入的 KB 摘要里没有相关信息，不要凭空捏造——直接告知"知识库里暂时没有这方面的记录"。\n\n' +

    '── 写入标准（通过知识库页面，不在对话中直接写入）──\n' +
    '以下信息适合写入知识库：\n' +
    '· 稳定知识点：不会很快过期的概念、方法、框架理解\n' +
    '· 项目核心信息：架构决策、重要约定、设计原则\n' +
    '· 阶段性总结：某个项目阶段的复盘与结论\n' +
    '以下内容不应进入知识库：\n' +
    '· 临时决策、本次 session 的具体步骤\n' +
    '· 情绪化的感悟（"今天效率很低"）\n' +
    '· 还未验证的推测或实验性结论\n\n' +

    '── 信息分类标签 ──\n' +
    '提议写入知识库时，建议给内容打标签：\n' +
    '[核心] 项目架构、关键设计原则——最高优先级，永不过期\n' +
    '[稳定] 学习总结、技术方法——长期有效，偶尔复查\n' +
    '[临时] 当前阶段的具体实施细节——有时效，过期可删\n\n' +

    '── 防止知识库污染 ──\n' +
    '不要把同一条信息重复写入多次（检查是否已有近似条目）。\n' +
    '当某条知识已过期或被推翻时，主动提醒用户更新或删除，而不是让错误信息一直在库里。'
  );

  var FALLBACK = '【知识库】KB 数据已注入，引用时基于注入内容，写入需稳定可复用，临时信息不写库。';

  window.AMADEUS_L2_KBSync = {
    name: 'L2_KBSync',
    layer: 'L2',
    build: function () { return PROMPT; },
    fallback: FALLBACK,
  };
})();
