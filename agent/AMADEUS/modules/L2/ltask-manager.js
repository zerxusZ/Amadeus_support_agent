/**
 * L2 · Long-term Task Manager
 * 职责：规定 agent 如何处理跨 session 的长期枝条任务
 * 输入：ctx（无强依赖；枝条数据已由 harness.js 注入 PRIMARY_REF）
 * 输出：prompt 文本块
 * 关系：依赖 L2/task-decomp.js 的分解结果；枝条写入通过 actions.js 的 create_branch 完成
 * Fallback：注入最小枝条处理说明
 */
(function () {
  'use strict';

  var PROMPT = (
    '【长期任务管理器（枝条）】\n' +

    '── 枝条是什么 ──\n' +
    '枝条（Branch）是多步骤的长期目标，由有序步骤（Steps）组成，步骤之间有依赖关系，只能线性推进。\n' +
    '典型枝条：项目开发、论文写作、技能习得、知识库建设等周期超过一周的事项。\n\n' +

    '── 枝条感知 ──\n' +
    'PRIMARY_REF 中已注入当前所有枝条的状态（名称、当前步、总步数）。\n' +
    '用户问到长期目标时，先检查是否有对应枝条，而不是从零开始规划——可能已经存在进行中的枝条。\n\n' +

    '── 枝条推进规则 ──\n' +
    '只有当前步骤完成后，才推进到下一步，不允许跳步。\n' +
    '用户问"接下来应该做什么"时，先给出当前枝条当前步骤的具体操作，而不是泛泛建议。\n' +
    '如果某步骤卡住了，帮用户拆解这一步，或建议是否需要调整枝条计划。\n\n' +

    '── 创建枝条的时机 ──\n' +
    '当目标明显需要超过 3 个步骤且跨越多天时，建议用枝条而不是单个任务。\n' +
    '创建前先口头描述枝条名称、步骤数、各步骤内容，等用户确认后再用 <aether_action> create_branch 落库。\n\n' +

    '── 长期任务的阶段性检查 ──\n' +
    '在枝条推进过程中，每隔几轮对话主动提一下当前枝条进度，帮用户保持对长期目标的感知。\n' +
    '不要让用户忘记自己有进行中的长期任务——但频率要自然，不能每句话都提。'
  );

  var FALLBACK = '【长期任务】检查已有枝条进度，按步骤推进，不跳步，创建前需用户确认。';

  window.AMADEUS_L2_LTaskManager = {
    name: 'L2_LTaskManager',
    layer: 'L2',
    build: function () { return PROMPT; },
    fallback: FALLBACK,
  };
})();
